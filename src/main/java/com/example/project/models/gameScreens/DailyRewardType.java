package com.example.project.models.gameScreens;

/**
 * Enum representing the types of daily rewards a player can receive.
 */
public enum DailyRewardType {

    /**
     * The player gets one shop item for free during the current shop round.
     */
    FREE_SHOP_ITEM,

    /**
     * The player receives $40 bonus money (Diamond reward).
     */
    DIAMOND_40,

    /**
     * The next valid word played earns double score and money.
     */
    LUCKY_WORD
}